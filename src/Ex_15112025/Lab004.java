package Ex_15112025;

public class Lab004 {
    public static void main(String[] args) {
        //Formating
        //%d  ---->int, byte, short, long
        //%c  ---->char
        //%s  ---->string
        //%f  ---->float
        int i=32;
        System.out.printf("int value is %d\n",i);

        int d=56;
        System.out.printf("sum value is %d\n",d+4);

        int y=43;
        char c='g';
        String s="new";
        float f=3.14f;
        System.out.printf("values are %d %c %s %f", y, c, s, f);
    }
}
